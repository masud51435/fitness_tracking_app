class DailyActivity {
  final String title;
  final String time;
  final double distance;
  final double totalDistance;
  final String calories;
  final String imagePath;
  final String unit;

  DailyActivity({
    required this.title,
    required this.time,
    required this.distance,
    required this.totalDistance,
    required this.calories,
    required this.imagePath,
    required this.unit,
  });
}
